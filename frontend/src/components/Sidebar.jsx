export default function Sidebar({ steps, current, completed, onNavigate }) {
  return (
    <nav className="flex-1 px-3 py-4 space-y-1">
      {steps.map((s) => {
        const isActive = s.id === current;
        const isDone = completed.includes(s.id);
        const isLocked = s.id > 1 && !completed.includes(s.id - 1);

        return (
          <button
            key={s.id}
            onClick={() => onNavigate(s.id)}
            disabled={isLocked}
            className={`w-full flex items-center gap-3 px-3 py-2.5 rounded-lg text-left transition-all duration-150 group
              ${isActive
                ? 'bg-blue-50 text-blue-700'
                : isDone
                  ? 'text-gray-700 hover:bg-gray-50'
                  : isLocked
                    ? 'text-gray-300 cursor-not-allowed'
                    : 'text-gray-500 hover:bg-gray-50 hover:text-gray-700'
              }`}
          >
            {/* Step indicator */}
            <div className={`w-8 h-8 rounded-lg flex items-center justify-center shrink-0 transition-all
              ${isActive
                ? 'bg-blue-600 text-white shadow-sm'
                : isDone
                  ? 'bg-green-50 text-green-600'
                  : 'bg-gray-100 text-gray-400'
              }`}>
              {isDone ? (
                <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" strokeWidth={2.5} stroke="currentColor">
                  <path strokeLinecap="round" strokeLinejoin="round" d="M4.5 12.75l6 6 9-13.5" />
                </svg>
              ) : (
                <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" strokeWidth={1.75} stroke="currentColor">
                  {ICONS[s.icon]}
                </svg>
              )}
            </div>

            <div className="min-w-0">
              <div className={`text-sm font-medium truncate ${isActive ? 'text-blue-700' : ''}`}>
                {s.label}
              </div>
              <div className={`text-[11px] truncate ${isActive ? 'text-blue-500' : 'text-gray-400'}`}>
                {isDone ? 'Completed' : s.desc}
              </div>
            </div>
          </button>
        );
      })}
    </nav>
  );
}
